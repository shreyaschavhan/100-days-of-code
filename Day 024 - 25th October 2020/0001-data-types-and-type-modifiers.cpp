#include <iostream>
using namespace std;

int main(){
    int a;
    cout << "The size of int is " << sizeof(a) << endl;
    float b;
    cout << "The size of float is " << sizeof(b) << endl;
    double c;
    cout << "The size of double is " << sizeof(c) << endl;
    char d;
    cout << "The size of char is " << sizeof(d) << endl;
    bool e;
    cout << "The size of bool is " << sizeof(e) << endl;
    short int si;
    long int li;
    cout << "The size of short int is " << sizeof(si) << endl;
    cout << "The size of long int is " << sizeof(li) << endl;
    long l;
    cout << "The size of long  is " << sizeof(l) << endl;
    return 0;
}
