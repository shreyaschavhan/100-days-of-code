#include <iostream>
#include <cstdio>
using namespace std;

int main() {
    int a;
    long b;
    char ch;
    float d;
    double e;
    scanf("%d %ld %c %f %lf", &a, &b, &ch, &d, &e);
    printf("%d\n", a);
    printf("%ld\n", b);
    printf("%c\n", ch);
    printf("%f\n", d);
    printf("%lf\n", e);

    return 0;
}
